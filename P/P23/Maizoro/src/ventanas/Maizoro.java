    private void btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn1ActionPerformed
        System.out.println(evt.getActionCommand()); 
        if(this.pantalla.getText().equals("0")) 
            this.pantalla.setText(evt.getActionCommand());
        else
            this.pantalla.setText(this.pantalla.getText() + evt.getActionCommand());
    }//GEN-LAST:event_btn1ActionPerformed

    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        System.out.println(evt.getKeyCode()); 
        if(evt.getKeyCode() >= 48 && evt.getKeyCode() <= 57) {
            // Un valor numÃ©rico
            if(this.pantalla.getText().equals("0")) 
                this.pantalla.setText(String.valueOf(evt.getKeyChar()));
            else
                this.pantalla.setText(this.pantalla.getText() + String.valueOf(evt.getKeyChar()));
        }
    }//GEN-LAST:event_formKeyPressed

